///basic calc functions

function Add(a, b) {
  return a + b;
}

function Subtract(a, b) {
  return a - b;
}

function Multiply(a, b) {
  return a * b;
}

function Divide(a, b) {
  return a / b;
}

console.log(Add(3, 4));
console.log(Subtract(10, 4));
console.log(Multiply(3, 4));
console.log(Divide(10, 2));
